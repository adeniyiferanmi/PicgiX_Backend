import mongoose from "mongoose";

const userShema = {
  fullName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
  confirmPassword: {
    type: String,
    required: true,
  },
};

const userModel = mongoose.model("users",userShema)
export default userModel
